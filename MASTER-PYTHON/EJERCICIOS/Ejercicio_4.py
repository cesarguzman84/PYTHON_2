# Ejercicio 4. Pedir 2 numeros al usuario y hacer todas las operaciones basicas de una calculadora y mostrarlo por pantalla

numero1 = int(input("Introduce el primer numero: "))
numero2 = int(input("Introduce el segundo numero: ")) 

print("###CALCULADORA###")
print("Suma: " + str(numero1+numero2))
print("Resta: " + str(numero1-numero2))
print("Multiplicacion: " + str(numero1*numero2))
print("Division: " + str(numero1/numero2))